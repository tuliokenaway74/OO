package org.example;

public class Adress {
    private String city;
    private String state;
    private String country;
    private String zipCode;
    private String adress;

    public Adress(String city, String state, String country, String zipCode, String adress) {
        this.city = city;
        this.state = state;
        this.country = country;
        this.zipCode = zipCode;
        this.adress = adress;
    }
}
